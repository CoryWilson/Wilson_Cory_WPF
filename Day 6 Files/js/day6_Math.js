//Always capitalize the M in Math.


//Math.round()
//round
//Normal Rounding .5 goes up, less goes down.
var num1 = 9.544;
console.log(num1);
console.log(Math.round(num1));

//Math.floor()
//floor
//Chops off all decimals and always rounds down.
var num2 = 6.99;
console.log(num2);
console.log(Math.floor(num2));

//Math.ceil()
//ceiling
//Always rounds up!
var num3 = 4.23;
console.log(num3);
console.log(Math.ceil(num3));
