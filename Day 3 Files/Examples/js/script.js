// Assignment Operators
/*
= Assignment
++ Adds 1 to the variable
-- Subtracts 1 from the variable
+= Addition Assignment
-= Subtract Assignment
/= Division Assignment
*= Multiplication Assignment
 */

var a = 1;
a++; // a = a+1
a--; // a = a-1
a+=4; // a = a+4
a-=3; // a = a-3
a/=2; // a = a/2
a*=6; // a = a*6
console.log(a);

